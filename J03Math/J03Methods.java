package J03Math;

public class J03Methods {
    public static void main(String[] args) {
        
        System.out.println(Math.max(10,50));
        System.out.println(Math.min(10,50));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.abs(-13.89));

        System.out.println(Math.random()); // 0 - 1 (random value print karse)
        System.out.println(Math.random()*11); // 0 - 10 (random value print karse)
        System.out.println((int)(Math.random()*11)); // 0 - 10 (integer --> random value print karse)
    }
}
